// Creating Class with name DisplayResult indicating the purpose is to display result
class DisplayResult {
    public static void main(String[] args) {

        // Create a string variable name and assign value Sam
        String name = "Sam";

        // Create a int variable rollNumber and assign value 1
        int rollNumber = 1;

        // Create a double variable percentMarks and assign value 99.99
        double percentMarks = 99.99;

        // Create a char variable result and assign value 'P' for pass
        char result = 'P';

        // Display the result
        System.out.println("Displaying Result:\n" + name + " with Roll Number " + rollNumber +
                           " has Scored " + percentMarks + "% Marks and Result is " + result);
    }
}